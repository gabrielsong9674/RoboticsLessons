
public class SwitchCases {
public static void main(String[] args) {
	for (int i = 11; i > 0; i --){
		switch(i) {
		case 1:
			System.out.println("i is 11");
			break;
		case 2:
			System.out.println("i is 10");
			break;
		case 3:
			System.out.println("i is 9");
			break;
		case 4:
			System.out.println("i is 8");
			break;
		case 5:
			System.out.println("i is less than 8");
			break;
			
			
	}
}
}
}
